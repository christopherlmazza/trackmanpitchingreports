        ("Avg Velo", lambda s: f"{s['RelSpeed'].dropna().mean():.1f} mph"),
        ("Max Velo",  lambda s: f"{s['RelSpeed'].dropna().max():.1f} mph"),
        ("Whiff%",    lambda s: f"{_fmt(_pct(( s['PitchCall']=='StrikeSwinging').sum(), s['PitchCall'].isin(_SWING_CALLS).sum()),'%')}"),
